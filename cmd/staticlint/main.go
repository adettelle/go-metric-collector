func main() {
	mychecks := getChecks()
	multichecker.Main(mychecks...)
}

func getChecks() []*analysis.Analyzer {
	mychecks := make([]*analysis.Analyzer, 0, 100)
	mychecks = append(mychecks,
		// Analyzers from golang.org/x/tools/go/analysis/passes
		appends.Analyzer,
		asmdecl.Analyzer,
		assign.Analyzer,
		atomic.Analyzer,
		atomicalign.Analyzer,
		bools.Analyzer,
		buildssa.Analyzer,
		buildtag.Analyzer,
		cgocall.Analyzer,
		composite.Analyzer,
		copylock.Analyzer,
		ctrlflow.Analyzer,
		deepequalerrors.Analyzer,
		defers.Analyzer,
		directive.Analyzer,
		errorsas.Analyzer,
		fieldalignment.Analyzer,
		findcall.Analyzer,
		framepointer.Analyzer,
		httpmux.Analyzer,
		httpresponse.Analyzer,
		ifaceassert.Analyzer,
		inspect.Analyzer,
		loopclosure.Analyzer,
		lostcancel.Analyzer,
		nilfunc.Analyzer,
		nilness.Analyzer,
		pkgfact.Analyzer,
		printf.Analyzer,
		reflectvaluecompare.Analyzer,
		shadow.Analyzer,
		shift.Analyzer,
		sigchanyzer.Analyzer,
		slog.Analyzer,
		sortslice.Analyzer,
		stdmethods.Analyzer,
		stdversion.Analyzer,
		stringintconv.Analyzer,
		structtag.Analyzer,
		testinggoroutine.Analyzer,
		tests.Analyzer,
		timeformat.Analyzer,
		unmarshal.Analyzer,
		unreachable.Analyzer,
		unsafeptr.Analyzer,
		unusedresult.Analyzer,
		unusedwrite.Analyzer,
		usesgenerics.Analyzer,
		// two public analyzers
		errwrap.Analyzer,
		critic.Analyzer,
		// analyzer finding expression os.Exit
		exitcheckanalyzer.ExitCheckAnalyzer,
	)
	// all SA analyzers of staticcheck.io
	for _, v := range staticcheck.Analyzers {
		mychecks = append(mychecks, v.Analyzer)
	}
	// analyzer of other packages from staticcheck.io
	for _, v := range staticcheck.Analyzers {
		if v.Analyzer.Name == "ST1013" {
			mychecks = append(mychecks, v.Analyzer)
		}
	}
	return mychecks
}
